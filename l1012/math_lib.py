from math import *

print(pi)  # число π
print(floor(1.8))  # округляет в пол - целое наименьшее
print(ceil(1.2))  # округляет в потолок - целое наибольшее
print(round(pi, 3))  # округляет (правильно) или до количества знаков
print(fabs(-5.64))  # модуль числа - abs() <- тоже модуль, но встроенный
print(pow(2, 9))  # возведение в степень. ** - возвращает int, pow - float
print(sqrt(121))  # квадратный корень
